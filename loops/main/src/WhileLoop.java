
import java.io.*;

public class WhileLoop {


  public static void main(String[] args) {
    int i = 0;

    while (i < 5){ /* break condition 5 < 5 : false : break the loop */
      System.out.println(i); // 0 , 1 , 2 , 3 , 4
      i++; // i = i +1; // i : 1 , 2 , 3 , 4 , 5
    }

    // System.out.println(i);

  }
}
